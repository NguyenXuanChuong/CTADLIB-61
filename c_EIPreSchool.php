      <p class="plainText"><strong>Family Centered Services</strong></p>
      
      <p class="plainText">Early Intervention Services work best when families are involved to meet the developmental goals of the child. Here are some guidelines for getting the most from the program.</p>
      
      <ul>
      <li class="plainText" style="padding-left:0px">Get Involved!  Be part of the process, have fun, give you child praise for trying new activities.</li>
      <li class="plainText" style="padding-left:0px">Make Every Minute Count!  When your child’s therapists works with your child, observes and try to activities that they recommend.  Ask questions and offer feedback on how your child is doing.</li>
      <li class="plainText" style="padding-left:0px">Ask for Help!  Let your therapist know if you or your child are struggling with their suggestions or activities.  Communication is the key!</li>
      <li class="plainText" style="padding-left:0px">Enjoy the Everyday!  There are ways of incorporating developmental skills into everything you do.  Your therapist can help you learn ways of helping your child develop while you carry out your daily routine.</li>
      <li class="plainText" style="padding-left:0px">Provide Variety!  Change the activities you do with your child and provide them with new and interesting views of their environment to keep them motivated to grow and develop.</li>
      <li class="plainText" style="padding-left:0px">Use your Support System!  Involve friends, family and other caretakers in helping your child meet their developmental outcomes.  </li>
      </ul>
      
      <p class="plainText"><em>The Early Intervention Program (EIP) is a public program for children under the age of three who are either suspected of having or at risk for developmental delays or disabilities. Potentially eligible children must be referred to the county program or the Growing Up Healthy Hotline at 1-800-522-5006 to receive EIP services. EIP is funded by New York State and county governments. All EIP services are provided at no cost to parents. Health insurance may be accessed for early intervention services provided to eligible children and families. A child's eligibility for the program can be determined only by state-approved evaluators under contract, and all services must be authorized by the county.<em></p>
