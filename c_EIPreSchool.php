      <p class="plainText">We know that your child’s development is very important to you. We’re here to help! The Early Intervention Program was first created in 1986 under the Individuals with Disabilities Education Act (IDEA) and has been in effect in New York since 1993. Children are eligible for services if they are under the age of three and have a confirmed developmental delay or a diagnosed physical or mental condition that has a high probability of resulting in a developmental delay.</p>
      <p class="plainText">Netcare<sup><font size="-4">&reg;</font></sup>, Inc. Early Intervention Program provides a variety of services to eligible children with developmental disabilities, from birth to age three, and their families. Netcare, Inc. is  approved as a provider of Early Intervention program services (EIP) by New York State to service the following areas and counties: Brooklyn, Queens, Bronx, Manhattan, Staten Island, Nassau, Suffolk, Rockland, Sullivan, Ulster, Orange, Westchester, and Dutchess.  Currently, Netcare, Inc. is under contract with the NYC municipality to deliver EI services.</p>
