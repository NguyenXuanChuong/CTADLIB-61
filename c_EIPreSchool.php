<a id="guide-to-growth"></a><p class="plainText"><strong>Guide to Growth</strong></p>
      
      <p class="plainText">Infants and toddlers develop and learn differently.  Most, however, are able to do certain things at different stages in their development.</p>
      
      <p class="plainText">For example:</p>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At three months, most babies:</strong></span>
      <li class="plainText" style="padding-left:0px">move both eyes in the same direction together</li>
      <li class="plainText" style="padding-left:0px">wiggle and kick with arms and legs</li>
      <li class="plainText" style="padding-left:0px">make cooing sounds</li>
      </ul>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At six months, they:</strong></span>
      <li class="plainText" style="padding-left:0px">reach for objects and pick them up</li>
      <li class="plainText" style="padding-left:0px">help hold the bottle during feeding</li>
      <li class="plainText" style="padding-left:0px">babble</li>
      </ul>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At 12 months, they:</strong></span>
      <li class="plainText" style="padding-left:0px">stand alone</li>
      <li class="plainText" style="padding-left:0px">wave “bye-bye”</li>
      <li class="plainText" style="padding-left:0px">know five or six words</li>
      </ul>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At 1 ½ years, most children:</strong></span>
      <li class="plainText" style="padding-left:0px">make marks on paper with crayon</li>
      <li class="plainText" style="padding-left:0px">like to look at pictures</li>
      <li class="plainText" style="padding-left:0px">walk without help</li>
      <li class="plainText" style="padding-left:0px">follow simple directions (“Bring me the ball”)</li>
      </ul>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At 2 years, they:</strong></span>
      <li class="plainText" style="padding-left:0px">say names of toys</li>
      <li class="plainText" style="padding-left:0px">like to imitate their parents</li>
      <li class="plainText" style="padding-left:0px">use two to three word sentences</li>
      </ul>
      
      <ul type="disc"><span class="plainText" style="padding-left:0px"><strong>At 3 years, they:</strong></span>
      <li class="plainText" style="padding-left:0px">put on their shoes</li>
      <li class="plainText" style="padding-left:0px">are toilet trained</li>
      <li class="plainText" style="padding-left:0px">use three to five word sentences</li>
      </ul>     
     
